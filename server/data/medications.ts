export const getMedicationsByCategory = (category: string): MedicationData[] => {
  return medicationsDatabase.filter(med => 
    med.category.toLowerCase().includes(category.toLowerCase())
  );
};

export const searchMedications = (query: string): MedicationData[] => {
  const searchLower = query.toLowerCase();
  return medicationsDatabase.filter(med =>
    med.name.toLowerCase().includes(searchLower) ||
    med.genericName.toLowerCase().includes(searchLower) ||
    med.brandNames.some(brand => brand.toLowerCase().includes(searchLower)) ||
    med.category.toLowerCase().includes(searchLower)
  );
};

export const getMedicationByName = (name: string): MedicationData | undefined => {
  return medicationsDatabase.find(med =>
    med.name.toLowerCase() === name.toLowerCase() ||
    med.genericName.toLowerCase() === name.toLowerCase() ||
    med.brandNames.some(brand => brand.toLowerCase() === name.toLowerCase())
  );
};

export const getControlledSubstances = (): MedicationData[] => {
  return medicationsDatabase.filter(med => med.isControlled);
};

export const getMedicationsWithBlackBoxWarning = (): MedicationData[] => {
  return medicationsDatabase.filter(med => med.blackBoxWarning);
};
